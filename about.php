    <div class="info">
        <p>Foreseeing the scope in the growth of Information Technology, Computer Science and Engineering department was established to offer Undergraduate Programme in Computer Science and Engineering way back in 1984. Initially, department started with an intake of 40 students. This department achieved phenomenal growth under the able administration.</p>
        <br>
        <p>Current intake of students for Computer Science and Engineering Department is: 120.To keep pace with technological trends in the IT sector, department is offering Graduate course (BE), two Post Graduate Programmes (M.Tech), namely, Computer Science & Engineering and Computer Network & Engineering and a Research Centre.</p>
        <br>
        <p>In the year 2013, Research Centre was sanctioned by VTU Belgaum to boost research activities in the field of Computer Science and Engineering. There are more than 50 research scholars have registerd for Ph.D. under the research center. One research scholar is awarded Ph.D. registered under CSE research center. Three more research scholars registered under CSE research center have submitted their thesis and waiting for final defence.</p>
        <br>
        <p>Department has highly qualified and experienced faculty with specializations in Image Processing , Computer Networking , Mobile computing and Data Analytics etc. There are eight faculty having Ph.D. degree. Almost all faculty members are persuing Ph.D. and most of them are on the verge of completing Ph.D. award.</p>
